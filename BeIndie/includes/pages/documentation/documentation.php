                <h3>1.1. Beschreibung des fiktiven Kunden und seiner Ziele</h3>
                <p>Unser fiktive Kunde ist eine Crowdfunding Firma. Diese Firma heißt BeIndie. Das Ziel von BeIndie ist es jedem der ein Projekt umsetzen möchte, dem
