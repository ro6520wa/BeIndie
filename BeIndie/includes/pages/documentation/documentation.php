                <h3>1.2. Beschreibung des fiktiven Kunden und seiner Ziele</h3>
                <p>Der spätere Nutzer kann sich einen Account auf der Seite erstellen um Projekte zu unterstützen oder selbst Projekte zu erstellen.
                Dabei erhält er ein eigenes Profil, welches er mit einem Profilbild und einer eigenen Beschreibung gestalten kann. Außerdem kann
                man auf dem Nutzerprofil die Projekte des Nutzer und Projekte die der Nutzer unterstützt einsehen. Ohne Account kann man die Suchfunktion
                der Seite benutzen sowie sich Profile und und Projekte anschauen. Man kann sich jedoch nicht Einloggen, Projekte unterstützen oder selbst
                Projekte erstellen.</p>
