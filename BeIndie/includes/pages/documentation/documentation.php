            <h2>7. Rollenmodell</h2>
            <p>Es gibt eingeloggte Nutzer und nicht eingeloggte Nutzer. Desweiteren Admins, welche über die php-myadmin Oberfläche vollen Zugriff auf
            die Datenbank haben. Eingeloggte Nutzer können alle Funktionen der Seite uneingeschränkt Nutzen. Nicht eingeloggte Nutzer können
            sich Registrieren, allerdings weder Projekte erstellen, noch Projekte unterstützen.</p>
        </div>
        <div id="herausforderungen">
            <h2>8. Bewältigte Herausforderungen</h2>
            <h3>Fixed Navbar</h3>
            <p><b>Problem:</b> Die Navbar sollte nach dem scrollen zu einem gewissen Punkt oben am Bildschirm "kleben" bleiben.</p>
            <p><b>Lösung:</b>jQuery Code, welcher die Scrollposition überprüft.</p>
            <h3>Dynamische Suche/Projekt unterstützen</h3>
            <p><b>Problem:</b> Die Suche sollte sich dynamisch bei Nutzereingabe durch Textinput oder Filterveränderung</p>
            <p><b>Lösung:</b> Via jQuery und Ajax wird bei Nutzereingaben der HTML Code in einer php-Funktion erzeugt.</p>
            <p><b>Problem:</b> Beim Eintragen von Umlauten (ä,ö,ü,ß) in die Datenbank (via Profil
            bearbeiten, Registrieren, Projekt erstellen/bearbeiten), werden diese nicht richtig erkannt und</p>
            <p><b>Lösung:</b> Setzen der Datenbankkollation auf utf8 via "mysqli_set_charset($conn, "utf8")"</p>
        </div>
